/**@brief Function for handling dfu events from the Buttonless Secure DFU service
 *
 * @param[in]   event   Event from the Buttonless Secure DFU service.
 */
static void ble_dfu_evt_handler(ble_dfu_buttonless_evt_type_t event)
{		
    switch (event)
    {
        case BLE_DFU_EVT_BOOTLOADER_ENTER_PREPARE:
        {
            NRF_LOG_INFO("Device is preparing to enter bootloader mode.");            
#ifdef UART_TRANS
            send_ble_data_to_st_byte(UART_CMD_DFU_STA,VALUE_PREPARE_DFU);
#endif
            // Prevent device from advertising on disconnect.
            ble_adv_modes_config_t config;
            advertising_config_get(&config);
            config.ble_adv_on_disconnect_disabled = true;
            ble_advertising_modes_config_set(&m_advertising, &config);

            // Disconnect all other bonded devices that currently are connected.
            // This is required to receive a service changed indication
            // on bootup after a successful (or aborted) Device Firmware Update.
            uint32_t conn_count = ble_conn_state_for_each_connected(disconnect, NULL);
            NRF_LOG_INFO("Disconnected %d links.", conn_count);
            break;
        }

        case BLE_DFU_EVT_BOOTLOADER_ENTER:
#ifdef UART_TRANS
            send_ble_data_to_st_byte(UART_CMD_DFU_STA,VALUE_ENTER_DFU);
#endif
            // YOUR_JOB: Write app-specific unwritten data to FLASH, control finalization of this
            //           by delaying reset by reporting false in app_shutdown_handler
            NRF_LOG_INFO("Device will enter bootloader mode.");
            break;

        case BLE_DFU_EVT_BOOTLOADER_ENTER_FAILED:
#ifdef UART_TRANS
            send_ble_data_to_st_byte(UART_CMD_DFU_STA,VALUE_ENTER_FAILED);
#endif
            NRF_LOG_ERROR("Request to enter bootloader mode failed asynchronously.");
            // YOUR_JOB: Take corrective measures to resolve the issue
            //           like calling APP_ERROR_CHECK to reset the device.
            break;

        case BLE_DFU_EVT_RESPONSE_SEND_ERROR:
#ifdef UART_TRANS
            send_ble_data_to_st_byte(UART_CMD_DFU_STA,VALUE_RSP_FAILED);
#endif						
            NRF_LOG_ERROR("Request to send a response to client failed.");
            // YOUR_JOB: Take corrective measures to resolve the issue
            //           like calling APP_ERROR_CHECK to reset the device.
            APP_ERROR_CHECK(false);
            break;

        default:
#ifdef UART_TRANS
            send_ble_data_to_st_byte(UART_CMD_DFU_STA,VALUE_UNKNOWN_ERR);
#endif
            NRF_LOG_ERROR("Unknown event from ble_dfu_buttonless.");
            break;
    }
}
